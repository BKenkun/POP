interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, replacements?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

// Helper function to get nested property safely
const getNestedTranslation = (obj: Translations, key: string): string | Translations | undefined => {
  return key.split('.').reduce<string | Translations | undefined>((acc, part) => {
    if (acc && typeof acc === 'object' && part in acc) {
      return (acc as Translations)[part];
    }
    return undefined;
  }, obj);
}

const translations: Record<Language, Translations> = {
  es: esTranslations,
  en: enTranslations,
  fr: frTranslations,
  de: deTranslations,
  it: itTranslations,
  pt: ptTranslations,
};

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguage] = useState<Language>('es');

  const setLanguageCallback = useCallback((lang: Language) => {
    setLanguage(lang);
  }, []);

  const t = useCallback((key: string, replacements: Record<string, string | number> = {}): string => {
    const selectedLanguageTranslations = translations[language] || translations.es;
    
    let translation = getNestedTranslation(selectedLanguageTranslations, key);

    // Fallback to Spanish if translation not found in the current language
    if (typeof translation !== 'string' && language !== 'es') {
        const fallbackTranslation = getNestedTranslation(translations.es, key);
        if (typeof fallbackTranslation === 'string') {
            translation = fallbackTranslation;
        }
    }
    
    // If still not found, return the key itself as a last resort
    if (typeof translation !== 'string') {
        console.warn(`Translation key not found in any language: ${key}`);
        return key;
    }

    let strResult = translation;
    for (const placeholder in replacements) {
        strResult = strResult.replace(`{${placeholder}}`, String(replacements[placeholder]));
    }
    return strResult;

  }, [language]);


  const value = useMemo(() => ({
    language,
    setLanguage: setLanguageCallback,
    t,
  }), [language, setLanguageCallback, t]);

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

